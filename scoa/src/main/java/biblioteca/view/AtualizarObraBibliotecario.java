    private void salvarButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButton1ActionPerformed
        // TODO add your handling code here:

        BibliotecarioController bibliotecario = new BibliotecarioController();

        try {
            statusObra status = null;
            if (statusBox.getSelectedItem().toString().toUpperCase().equals("DISPONÍVEL"))status = statusObra.DISPONÍVEL;
            else if (statusBox.getSelectedItem().toString().toUpperCase().equals("INDISPONÍVEL")) status = statusObra.INDISPONÍVEL;
            else JOptionPane.showMessageDialog(this, "Status inválido! (DISPONÍVEL / INDISPONÍVEL)");
            
            Integer obraId = Integer.parseInt(invisivelBox.getItemAt(obraBox.getSelectedIndex()));

            bibliotecario.atualizarObra(obraId, tituloField.getText(), autorField.getText(), tipoMaterialField.getText().toUpperCase(), editoraField.getText(),
                anoField.getText(), localizacaoField.getText(), status, deletedCheck.isSelected());

        }
        catch (Exception e ){
            JOptionPane.showMessageDialog(this, "Dados inválidos!");
        }
    }//GEN-LAST:event_salvarButton1ActionPerformed

