    private void salvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButtonActionPerformed
        // TODO add your handling code here:

        BibliotecarioController bibliotecario = new BibliotecarioController();

        try {
            LocalDate nascimento = null;
            try {
                nascimento = LocalDate.parse(nascimentoField.getText());
            }
            catch (Exception e){
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Data inválida! Digite no formato aaaa-mm-dd");

            }
            
            Integer bibliotecarioId = Integer.parseInt(idField.getText());
            
            if (deletedCheck.isSelected() && loginField.getText().equals(getUsuarioLogado().getLogin())){
                JOptionPane.showMessageDialog(this, "Você não pode deletar a si mesmo!");
            }
            else {

            bibliotecario.atualizarBibliotecario( bibliotecarioId, nomeField.getText(),cpfField.getText(),rgField.getText(), nascimento,
                enderecoField.getText(), emailField.getText(), contatoField.getText(), loginField.getText(), senhaField.getText(), deletedCheck.isSelected() );
            }

        }
        catch (Exception e ){
            JOptionPane.showMessageDialog(this, "Dados inválidos!");
        }
    }//GEN-LAST:event_salvarButtonActionPerformed

